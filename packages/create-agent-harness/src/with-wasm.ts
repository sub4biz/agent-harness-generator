// GH #25: `--with-wasm <crate-path>` wires a project's own wasm-pack crate into
// the scaffolded harness as real, loadable capability commands.
//
// It (1) builds the crate with `wasm-pack --target nodejs` into <project>/wasm
// (clearing RUSTFLAGS so a workspace mold linker doesn't break the wasm32
// rust-lld link), (2) drops wasm-pack's ignore-everything `.gitignore` and
// writes a {"type":"commonjs"} marker so the CJS shim loads under an ESM
// harness (GH #21), (3) writes a `bin/wasm.mjs` loader exposing the crate's
// exports as subcommands, and (4) adds `wasm/**` to the harness `files` and a
// `wasm` doctor check. Missing wasm-pack/cargo is reported, not fatal.

import { execSync } from 'node:child_process';
import { existsSync, rmSync, writeFileSync, readFileSync, mkdirSync } from 'node:fs';
import { resolve, join } from 'node:path';

export interface WasmWireResult {
  ok: boolean;
  lines: string[];
}

function have(cmd: string): boolean {
  try {
    execSync(cmd, { stdio: 'ignore' });
    return true;
  } catch {
    return false;
  }
}

export function wireWasm(cratePath: string, targetDir: string): WasmWireResult {
  const lines: string[] = [];
  const crate = resolve(process.cwd(), cratePath);
  if (!existsSync(join(crate, 'Cargo.toml'))) {
    return { ok: false, lines: [`--with-wasm: no Cargo.toml at ${crate}`] };
  }
  if (!have('wasm-pack --version')) {
    return {
      ok: false,
      lines: [
        '--with-wasm: wasm-pack not found on PATH.',
        '  Install it (https://rustwasm.github.io/wasm-pack/installer/) and re-run,',
        '  or build wasm/ yourself: wasm-pack build <crate> --target nodejs --out-dir wasm',
      ],
    };
  }

  const outDir = join(targetDir, 'wasm');
  mkdirSync(outDir, { recursive: true });
  try {
    execSync(
      `wasm-pack build ${JSON.stringify(crate)} --target nodejs --release --out-dir ${JSON.stringify(outDir)}`,
      { stdio: 'inherit', env: { ...process.env, RUSTFLAGS: '' } },
    );
  } catch (e) {
    return { ok: false, lines: [`--with-wasm: wasm-pack build failed: ${(e as Error).message}`] };
  }

  // GH #21: the nodejs target is CommonJS. wasm-pack drops a `.gitignore`
  // containing "*" (which would also prune wasm/ from any npm tarball) and a
  // package.json without a type field. Remove the .gitignore and force
  // type:commonjs so the CJS shim loads correctly under an ESM harness.
  rmSync(join(outDir, '.gitignore'), { force: true });
  const wasmPkgPath = join(outDir, 'package.json');
  let wasmPkg: Record<string, unknown> = {};
  try {
    wasmPkg = JSON.parse(readFileSync(wasmPkgPath, 'utf8'));
  } catch {
    /* wasm-pack always writes one; tolerate absence */
  }
  wasmPkg.type = 'commonjs';
  const entry = typeof wasmPkg.main === 'string' && wasmPkg.main ? (wasmPkg.main as string) : 'index.js';
  writeFileSync(wasmPkgPath, JSON.stringify(wasmPkg, null, 2) + '\n');

  // Add wasm/** to the harness package.json `files` (so it publishes) and a
  // `wasm` script that runs the loader.
  const harnessPkgPath = join(targetDir, 'package.json');
  try {
    const hpkg = JSON.parse(readFileSync(harnessPkgPath, 'utf8')) as Record<string, unknown>;
    const files = Array.isArray(hpkg.files) ? (hpkg.files as string[]) : [];
    if (!files.includes('wasm/**')) files.push('wasm/**');
    hpkg.files = files;
    const scripts = (hpkg.scripts as Record<string, string>) || {};
    if (!scripts.wasm) scripts.wasm = 'node bin/wasm.mjs';
    hpkg.scripts = scripts;
    writeFileSync(harnessPkgPath, JSON.stringify(hpkg, null, 2) + '\n');
  } catch {
    lines.push('--with-wasm: could not update harness package.json files/scripts');
  }

  // bin/wasm.mjs: a CLI that loads the CJS wasm shim and exposes each exported
  // function as a subcommand (`node bin/wasm.mjs <fn> [json-args...]`).
  const loader = `#!/usr/bin/env node
// Auto-generated by metaharness --with-wasm. Loads the bundled wasm-pack crate
// (CommonJS, --target nodejs) and exposes its exports as subcommands.
import { createRequire } from 'node:module';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const require = createRequire(import.meta.url);
const here = dirname(fileURLToPath(import.meta.url));
const wasm = require(join(here, '..', 'wasm', ${JSON.stringify(entry)}));

const fns = Object.keys(wasm).filter((k) => typeof wasm[k] === 'function');
const [, , sub, ...rest] = process.argv;

if (!sub || sub === '--help' || sub === '-h') {
  console.log('Wasm capability commands (from your crate):');
  for (const f of fns) console.log('  node bin/wasm.mjs ' + f + ' [arg ...]');
  process.exit(sub ? 0 : 1);
}
if (typeof wasm[sub] !== 'function') {
  console.error('Unknown wasm export "' + sub + '". Available: ' + fns.join(', '));
  process.exit(2);
}
// Args are passed through; numeric-looking args are coerced to numbers.
const args = rest.map((a) => (a !== '' && !Number.isNaN(Number(a)) ? Number(a) : a));
const out = wasm[sub](...args);
console.log(typeof out === 'string' ? out : JSON.stringify(out, null, 2));
`;
  mkdirSync(join(targetDir, 'bin'), { recursive: true });
  writeFileSync(join(targetDir, 'bin', 'wasm.mjs'), loader);

  // A doctor check the harness can surface: confirm the wasm loads + lists fns.
  const doctorCheck = `// Auto-generated by metaharness --with-wasm. Returns the loadable wasm exports
// (used by \`harness doctor\` to confirm the wasm capability is wired).
import { createRequire } from 'node:module';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
const require = createRequire(import.meta.url);
const here = dirname(fileURLToPath(import.meta.url));
export function wasmDoctor() {
  try {
    const wasm = require(join(here, '..', 'wasm', ${JSON.stringify(entry)}));
    const fns = Object.keys(wasm).filter((k) => typeof wasm[k] === 'function');
    return { ok: true, exports: fns };
  } catch (e) {
    return { ok: false, error: e instanceof Error ? e.message : String(e) };
  }
}
`;
  mkdirSync(join(targetDir, 'src'), { recursive: true });
  writeFileSync(join(targetDir, 'src', 'wasm-doctor.mjs'), doctorCheck);

  lines.push(
    `--with-wasm: built ${crate} → wasm/ (target nodejs), marked commonjs, dropped wasm-pack .gitignore.`,
    `  Added wasm/** to files, wrote bin/wasm.mjs loader + src/wasm-doctor.mjs.`,
    `  Try: node bin/wasm.mjs --help`,
  );
  return { ok: true, lines };
}
